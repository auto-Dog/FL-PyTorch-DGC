from options import args_parser
from update import LocalUpdate, test_inference
from models import MLP, CNNMnist, CNNFashion_Mnist, CNNCifar
from utils import get_dataset, average_weights, exp_details, average_weights_gradient,GradientList


if __name__ == '__main__':
    start_time = time.time()

    # define paths
    path_project = os.path.abspath('..')
    logger = SummaryWriter('../logs')

    args = args_parser()
    exp_details(args)

    # if args.gpu:
    #     torch.cuda.set_device(args.gpu) # 输入格式是str 'cuda:0,1,...'
    device = 'cuda' if args.gpu else 'cpu'

    # load dataset and user groups
    train_dataset, test_dataset, user_groups = get_dataset(args)

    # BUILD MODEL
    if args.model == 'cnn':
        # Convolutional neural netork
        if args.dataset == 'mnist':
            global_model = CNNMnist(args=args)
        elif args.dataset == 'fmnist':
            global_model = CNNFashion_Mnist(args=args)
        elif args.dataset == 'cifar':
            # global_model = CNNCifar(args=args)
            from torchvision import models
            global_model = models.resnet50(num_classes = 10)    # 和DGC实验保持一致

    elif args.model == 'mlp':
        # Multi-layer preceptron
        img_size = train_dataset[0][0].shape
        len_in = 1
        for x in img_size:
            len_in *= x
            global_model = MLP(dim_in=len_in, dim_hidden=64,
                               dim_out=args.num_classes)
    else:
        exit('Error: unrecognized model')

    # Set the model to train and send it to device.
    # global_model.to(device)
    global_model = global_model.cuda()
    global_model.train()
    # print(global_model)

    # copy weights
    global_weights = global_model.state_dict()

    # Training
    train_loss, train_accuracy = [], []
    val_acc_list, net_list = [], []
    cv_loss, cv_acc = [], []
    print_every = 2
    val_loss_pre, counter = 0, 0

    # local_gradient_store = GradientList(args.num_users) 
    # momentum_gradient_store = GradientList(args.num_users) 
    local_gradient_store = [None for i in range(args.num_users)]
    momentum_gradient_store = [None for i in range(args.num_users)]
    for epoch in tqdm(range(args.epochs),ascii=True):
        local_weights, local_losses = [], []
        # print(f'\n | Global Training Round : {epoch+1} |\n')

        global_model.train()
        m = max(int(args.frac * args.num_users), 1)
        idxs_users = np.random.choice(range(args.num_users), m, replace=False)

        for idx in idxs_users:
            local_model = LocalUpdate(args=args, dataset=train_dataset,
                                      idxs=user_groups[idx], logger=logger)
            g_updete, g_store,g_momen, loss = local_model.update_weights(
                model=copy.deepcopy(global_model), global_round=epoch, gradient_store=local_gradient_store[idx],
                last_update=momentum_gradient_store[idx])
            local_gradient_store[idx] = g_store
            momentum_gradient_store[idx] = g_momen  # 储存动量累积值
            local_weights.append(copy.deepcopy(g_updete))   # 这里的local weights实际存储weights的更新值
            local_losses.append(copy.deepcopy(loss))
        torch.cuda.empty_cache()    # 清空缓存，防止变量累积

        # update global weights
        # global_weights = average_weights(local_weights)
        global_weights = average_weights_gradient(global_model.state_dict(),local_weights)
        # update global weights
        global_model.load_state_dict(global_weights)

        loss_avg = sum(local_losses) / len(local_losses)
        train_loss.append(loss_avg)

        # Calculate avg training accuracy over all users at every epoch
        list_acc, list_loss = [], []
        global_model.eval()
        for c in range(args.num_users):
            local_model = LocalUpdate(args=args, dataset=train_dataset,
                                      idxs=user_groups[idx], logger=logger)
            acc, loss = local_model.inference(model=global_model)
            list_acc.append(acc)
            list_loss.append(loss)
        train_accuracy.append(sum(list_acc)/len(list_acc))

        # print global training loss after every 'i' rounds
        if (epoch+1) % print_every == 0:
            print(f' \nAvg Training Stats after {epoch+1} global rounds:')
            print(f'Training Loss : {np.mean(np.array(train_loss))}')
            print('Train Accuracy: {:.2f}% \n'.format(100*train_accuracy[-1]))
        
        if (epoch%20 == 0 and epoch>0):
            torch.save(global_model.state_dict(),'./model_e{}_fed_DGC.pth'.format(epoch))

    # torch.save(local_weights[0],'./a_update_weight_resnet50.pth') # 保存一轮通信中上传数据包，以查看大小

    # Test inference after completion of training
    test_acc, test_loss = test_inference(args, global_model, test_dataset)

    print(f' \n Results after {args.epochs} global rounds of training:')
    print("|---- Avg Train Accuracy: {:.2f}%".format(100*train_accuracy[-1]))
    print("|---- Test Accuracy: {:.2f}%".format(100*test_acc))

    # Saving the objects train_loss and train_accuracy:
    file_name = './save/objects/{}_{}_{}_C[{}]_iid[{}]_E[{}]_B[{}].pkl'.\
        format(args.dataset, args.model, args.epochs, args.frac, args.iid,
               args.local_ep, args.local_bs)

    with open(file_name, 'wb') as f:
        pickle.dump([train_loss, train_accuracy], f)
    np.savetxt('./save/acc_and_loss_feddgc_m.csv',np.array([train_loss, train_accuracy]),delimiter=',')  # 输出结果用于绘图
    print('\n Total Run Time: {0:0.4f}'.format(time.time()-start_time))

